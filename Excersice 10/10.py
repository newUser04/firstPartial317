import multiprocessing as mp;
from multiprocessing import Pool;

def generate(init, end):
  v = [];
  for i in range(init, end):
    v.append((i + 1) * 2);
  return v;

if __name__ == '__main__':
  print("Ingress N: ");
  n = int(input());

  parameters = [((i * n), (n * (i + 1))) for i in range (mp.cpu_count())];

  pool = Pool(processes = mp.cpu_count());

  result = pool.starmap(generate, parameters);

  print("Processors: " + str(mp.cpu_count()));
  for i in range(len(result)):
    print("Vector in processor: " + str(i));
    print("\t", result[i]);


